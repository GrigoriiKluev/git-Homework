def create_file():
    n = (random.randint(0, 9) for i in range(2500))
    my_str = ''.join(str(i) for i in n)
    with open('test.txt', 'w', ) as f:
        f.write(my_str)


def read_file():
    with open('test.txt', 'r') as myfile:
        return myfile.read().replace('\n', '')


def main():
    create_file()
    input = read_file()
    print(input)
    array = [int(d) for d in input]
    longest_sequence = []
    candidate_sequence = []
    prev = None
    for el in array:
        if prev != el:
            if len(longest_sequence) < len(candidate_sequence):
                longest_sequence = candidate_sequence
            candidate_sequence = []
        candidate_sequence.append(el)
        prev = el
    print(longest_sequence)
print(main())